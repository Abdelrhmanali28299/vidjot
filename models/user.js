const mongoose = require('mongoose')
const Schema = mongoose.Schema

let user = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now()
    }
})

const modelClass = mongoose.model('users', user)

module.exports = modelClass;